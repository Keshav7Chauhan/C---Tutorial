// C++ program to Demonstrate for loop
#include <iostream>
using namespace std;

int main()
{
	for (int i = 1; i <= 5; i++) {
		cout << "Hello World\n";
	}

	return 0;
}


// C++ program to Demonstrate while loop
#include <iostream>
using namespace std;

int main()
{
	// initialization expression
	int i = 1;

	// test expression
	while (i < 6) {
		cout << "Hello World\n";

		// update expression
		i++;
	}

	return 0;
}
